#!/usr/bin/env node
// Validate every hardcoded production docs URL (https://promptkit.altairalabs.ai/...)
// that appears ANYWHERE in the repo against the freshly built docs site in dist/.
//
// This closes a gap in check-links.js: that crawler only follows links *within*
// the built site and deliberately skips absolute promptkit.altairalabs.ai URLs
// (canonical/og:url tags 404 until deploy). But scaffolding templates
// (tools/arena/templates/**), package READMEs (npm/**), and example READMEs
// hardcode absolute doc URLs that the crawler never sees — so a renamed or
// mistyped page (e.g. /arena/guides/tools, /arena/reference/scenario) ships
// broken into every `promptarena init` project without CI noticing.
//
// We resolve each URL's path against dist/ (Astro builds directory-format pages
// at <path>/index.html), so this runs offline and doesn't depend on the live
// site being deployed.

import { execSync } from 'child_process';
import { existsSync, readFileSync, statSync } from 'fs';
import { join, resolve } from 'path';

const DOCS_DIR = resolve(import.meta.dirname);
const REPO_ROOT = resolve(DOCS_DIR, '..');
const DIST = join(DOCS_DIR, 'dist');

if (!existsSync(DIST)) {
  console.error(`❌ ${DIST} not found — run \`make docs-build\` before this check.`);
  process.exit(1);
}

const SITE = 'https://promptkit.altairalabs.ai';
const URL_RE = /https:\/\/promptkit\.altairalabs\.ai(\/[A-Za-z0-9/_.-]*)?/g;

// Paths we cannot resolve against the Starlight dist:
//  - /schemas/** are generated JSON served from a separate pipeline
//  - /sitemap* are generated by the sitemap integration
const SKIP_PREFIXES = ['/schemas', '/sitemap'];

const shouldSkip = (path) => SKIP_PREFIXES.some((p) => path === p || path.startsWith(p + '/') || path.startsWith(p));

// A doc path resolves if dist has any of:
//   <path>/index.html   (directory-format page — the Starlight default)
//   <path>.html         (file-format page)
//   <path>              (a real asset/file, e.g. an image)
// The site root "/" always resolves to dist/index.html.
const resolvesInDist = (path) => {
  const clean = path.replace(/\/+$/, '') || '/';
  if (clean === '/') return existsSync(join(DIST, 'index.html'));
  const rel = clean.replace(/^\//, '');
  if (existsSync(join(DIST, rel, 'index.html'))) return true;
  if (existsSync(join(DIST, rel + '.html'))) return true;
  const direct = join(DIST, rel);
  return existsSync(direct) && statSync(direct).isFile();
};

// The link-check scripts themselves contain illustrative/placeholder URLs
// (e.g. https://promptkit.altairalabs.ai/... in comments) that aren't real
// links — don't scan them.
const SELF_EXCLUDE = new Set(['docs/check-doc-urls.js', 'docs/check-links.js']);

const files = execSync('git ls-files', { cwd: REPO_ROOT, encoding: 'utf8' })
  .split('\n')
  .filter(Boolean)
  // Don't lint the docs site's own source — those relative links are covered
  // by check-links.js, and dist/ is build output.
  .filter((f) => !f.startsWith('docs/dist/') && !SELF_EXCLUDE.has(f));

// url -> Set of "file:line" where it appears
const broken = new Map();
let urlsChecked = 0;

for (const file of files) {
  const abs = join(REPO_ROOT, file);
  let content;
  try {
    content = readFileSync(abs, 'utf8');
  } catch {
    continue; // binary / unreadable
  }
  if (!content.includes(SITE)) continue;

  const lines = content.split('\n');
  lines.forEach((line, idx) => {
    for (const match of line.matchAll(URL_RE)) {
      const path = match[1] ?? '/';
      if (shouldSkip(path)) continue;
      urlsChecked++;
      if (resolvesInDist(path)) continue;
      if (!broken.has(path)) broken.set(path, new Set());
      broken.get(path).add(`${file}:${idx + 1}`);
    }
  });
}

console.log(`🔍 Checked ${urlsChecked} hardcoded ${SITE} doc URLs against dist/\n`);

if (broken.size === 0) {
  console.log('✅ No broken hardcoded doc URLs found!');
  process.exit(0);
}

console.log(`❌ Found ${broken.size} broken hardcoded doc URL(s):\n`);
for (const [path, locations] of [...broken].sort()) {
  console.log(`  [404] ${SITE}${path}`);
  [...locations].sort().forEach((loc) => console.log(`        - ${loc}`));
  console.log('');
}
console.log('Fix the URL to a path that exists in the built site, or remove the link.');
process.exit(1);
